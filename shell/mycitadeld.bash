                return 0
            fi
            case "${prev}" in
                
                --format)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                    -f)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        mycitadel__wallet__rename)
            opts=" -v -h -V  --verbose --help --version  <wallet-id> <name> "
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then

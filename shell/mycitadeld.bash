                ;;
            mark-used)
                cmd+="__mark__used"
                ;;
            rename)
                cmd+="__rename"
                ;;
            single-sig)
                cmd+="__single__sig"
                ;;
            wallet)
                cmd+="__wallet"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        mycitadel)
            opts=" -v -T -x -n -d -c -h -V  --verbose --tor-proxy --rpc-endpoint --chain --init --data-dir --electrum-server --rgb20-endpoint --config --help --version  wallet address asset help"

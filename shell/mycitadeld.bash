_mycitadel() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            mycitadel)
                cmd="mycitadel"
                ;;
            
            address)
                cmd+="__address"
                ;;
            asset)
                cmd+="__asset"
                ;;
            balance)
                cmd+="__balance"
                ;;
            create)
                cmd+="__create"
                ;;
